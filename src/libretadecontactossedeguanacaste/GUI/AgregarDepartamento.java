    private void AgregarNuevoDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarNuevoDepartamentoActionPerformed

        if(SiElNombreExiste(txtDepartamento.getText()))
        {
            JOptionPane.showMessageDialog(null, "El nombre del departamento ya esta registrado");
              
        }else if(txtDepartamento.getText().equalsIgnoreCase(""))
        {
           JOptionPane.showMessageDialog(null, "no debe dejar el nombre vacio");
             
        }else
        {
             
            Departamentos elNuevoDepartamento=new Departamentos();

      
        elNuevoDepartamento.setDepartamento(txtDepartamento.getText());
        elNuevoDepartamento.setIdinstancia(ObtenerIdInstancia());
        
        GestorDeDeartamentos.IngresarDepartamentos(elNuevoDepartamento);

        }
        
        
        LlenarListaDeDepartamentos();
    }//GEN-LAST:event_AgregarNuevoDepartamentoActionPerformed

    private boolean SiElNombreExiste(String Nombre)
{
    boolean Existe=false;
    GestorDeDeartamentos=new GestorDeDepartamentos(); 
   
    for (Departamentos departamento : GestorDeDeartamentos.ObtenerLosDepartamentos()) {
        try {
            if(departamento.getDepartamento().equalsIgnoreCase(Nombre))
        {
            Existe=true;
        }
        } catch (NullPointerException e) {
            //JOptionPane.showMessageDialog(null, "el campo esta nulo");
        }
        
    }
   
   
   
   return Existe;
}
    public void ObtenerDatos(){
  Departamentos elDepartamento=new Departamentos();
  
        try {
          elDepartamento=(Departamentos)JListaDeDepartamentos.getSelectedValue();
          
        } catch (Exception e) {
        }
  
  txtDepartamento.setText(elDepartamento.getDepartamento());
  CbInstancias.setSelectedItem(ObtieneNombreDeInstancia(elDepartamento.getIdinstancia()));
          
    
    }
    
    public Instancias ObtieneNombreDeInstancia(int idInstancia)
    { 
        Instancias NombreDeLaInstancia=null;
     GestorDeInstancias gestorDeInstancias=new GestorDeInstancias();
        for (Instancias laInstancia : gestorDeInstancias.ObtenerLasInstancias()) {
            
            if(laInstancia.getIdinstancia()==idInstancia)
            {
                NombreDeLaInstancia=laInstancia;
            }
        }
       return NombreDeLaInstancia; 
    }
    
