private void ListarInstanciasParaComboBox()
    {
        GestorDeInstancias GestorDeInstancias= new GestorDeInstancias();
         Instancias laInstancia= new Instancias();
     
        try {
            for (int i = 0; i < GestorDeInstancias.ObtenerLasInstancias().size(); i++) {
             
            laInstancia= GestorDeInstancias.ObtenerLasInstancias().get(i);
            CbInstancias.addItem(laInstancia);
              
        }
        } catch (Exception e) {
        }
         
         
        
    }
    
    
    
