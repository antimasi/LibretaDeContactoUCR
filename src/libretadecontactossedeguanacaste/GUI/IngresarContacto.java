    private boolean SiElNombreExiste(String Nombre)
{
    boolean Existe=false;
   GestorDeContactos elContacto=new GestorDeContactos(); 
   
    for (Contactos contacto : elContacto.ObtenerLasContactos()) {
        if(contacto.getContacto().equalsIgnoreCase(Nombre))
        {
            Existe=true;
        }
    }
   
   
   
   return Existe;
}
   
public int IdCOntacto()
{
    int idContacto=0;
 GestorDeContactos elContacto=new GestorDeContactos(); 
 for (Contactos contacto : elContacto.ObtenerLasContactos()) {
        if(contacto.getContacto().equalsIgnoreCase(txtNombre.getText()))
        {
            idContacto=contacto.getIdcontacto();
        }
    }
 return idContacto;
}

    
