    private void BtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAgregarActionPerformed
       GestorDeContactos elContacto=new GestorDeContactos();
       Contactos elNuevoContacto=new Contactos();
        if(ElNombreEstaCorrecto(txtNombre.getText()))
        {
            
               elNuevoContacto.setIddepartamento(ObtenerIdDepartamento());  
      
               elNuevoContacto.setIdcarrera(ObtenerIdCarrera());  
           
            elNuevoContacto.setContacto(txtNombre.getText());
            elNuevoContacto.setEsDelSistema(Sistema());
            
         elContacto.IngresarDepartamentos(elNuevoContacto);
           JOptionPane.showMessageDialog(null, "Contacto registrado");
           LlenarTelefonos_Correos();
        }
    }//GEN-LAST:event_BtAgregarActionPerformed

    public boolean ElNombreEstaCorrecto(String txtDeIngreso)
    {
        boolean EsCorrecto=false;
        if(SiElNombreExiste(txtDeIngreso))
        {
            JOptionPane.showMessageDialog(null, "El nombre ya esta registrado");
              
        }else if(txtDeIngreso.equalsIgnoreCase(""))
        {
           JOptionPane.showMessageDialog(null, "no debe dejar el nombre vacio");
             
        }else{EsCorrecto=true;}
        
        
        return EsCorrecto;
    }
    
    
    
    
